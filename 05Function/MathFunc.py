#Math Package

from math import *
from random import *

print('------------------------------')
print('max(10, 20): ', max(10, 20)) # Find max
print('min(10, 20): ', min(10, 20)) # Find min
print('sum(range(1, 4)): ', sum(range(1, 4))) # Sum
print('------------------------------')
print('ceil(1.45): ', ceil(1.45)) # round up
print('floor(1.45): ', floor(1.45)) # round floor
print('round(1.45): ', round(1.45)) # round up if more than 0.5 else round floor
print('------------------------------')
print('pow(2, 3): ', pow(2, 3)) #exponent
print('sqrt(100): ', sqrt(100)) # square root
print('abs(-1): ', abs(-1)) # absolute
print('------------------------------')
print('randint(1, 3): ', randint(1, 3)) # random int
print('random(): ', random()) # random decimal